    private void txtTelefonoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtTelefonoMousePressed
        if(txtTelefono.getText().equals("Ingrese su numero telefonico")){
            txtTelefono.setText("");
            txtTelefono.setForeground(Color.BLACK);
        }
        if(txtNombre.getText().isEmpty()){
            txtNombre.setText("Ingrese su nombre completo");
            txtNombre.setForeground(Color.gray);
        }
        if(txtCorreo.getText().isEmpty()){
            txtCorreo.setText("Ingrese su correo electronico");
            txtCorreo.setForeground(Color.gray);
        }
        if(txtApodo.getText().isEmpty()){
            txtApodo.setText("Ingrese su apodo");
            txtApodo.setForeground(Color.gray);
        }
        if(String.valueOf(txtPass.getPassword()).isEmpty()){
            txtPass.setText("Ingrese su contrasena");
            txtPass.setForeground(Color.gray); 
        }
        if(String.valueOf(txtPassConf.getPassword()).isEmpty()){
            txtPassConf.setText("Ingrese su contrasena");
            txtPassConf.setForeground(Color.gray); 
        }
    }//GEN-LAST:event_txtTelefonoMousePressed

