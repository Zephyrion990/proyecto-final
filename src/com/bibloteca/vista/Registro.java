    private void txtNombreMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtNombreMousePressed
        if(txtNombre.getText().equals("Ingrese su nombre completo")){
            txtNombre.setText("");
            txtNombre.setForeground(Color.BLACK);
        }
        if(txtCorreo.getText().isEmpty()){
            txtCorreo.setText("Ingrese un correo electronico");
            txtCorreo.setForeground(Color.gray);
        }
        if(txtApodo.getText().isEmpty()){
            txtApodo.setText("Ingrese su apodo");
            txtApodo.setForeground(Color.gray);
        }
        if(txtTelefono.getText().isEmpty()){
            txtTelefono.setText("Ingrese su numero telefonico");
            txtTelefono.setForeground(Color.gray);
        }
        if(String.valueOf(txtPass.getPassword()).isEmpty()){
            txtPass.setText("Ingrese su contrasena");
            txtPass.setForeground(Color.gray); 
        }
        if(String.valueOf(txtPassConf.getPassword()).isEmpty()){
            txtPassConf.setText("Ingrese su contrasena");
            txtPassConf.setForeground(Color.gray); 
        }
    }//GEN-LAST:event_txtNombreMousePressed

